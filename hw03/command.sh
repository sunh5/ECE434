#!/bin/bash
#This is to set the upper and lower temperatures. 
#Run this before run alarm.py, and reboot the board. 
i2cset -y 2 0x48 0x01 0x80
i2cset -y 2 0x4a 0x01 0x80
i2cset -y 2 0x48 0x03 0x1a
i2cset -y 2 0x48 0x02 0x14
i2cset -y 2 0x48 0x01 0x80
i2cset -y 2 0x4a 0x03 0x18
i2cset -y 2 0x4a 0x02 0x14
i2cset -y 2 0x4a 0x01 0x80